@Composable
fun BiliUserLevelIcon(
    level: Int,
    modifier: Modifier = Modifier,
    width: Dp = 34.dp,
    height: Dp = 22.dp,
) {
    if (level <= 0) return
    val icon = when (level.coerceIn(1, 6)) {
        1 -> BiliUserLevel1Icon
        2 -> BiliUserLevel2Icon
        3 -> BiliUserLevel3Icon
        4 -> BiliUserLevel4Icon
        5 -> BiliUserLevel5Icon
        else -> BiliUserLevel6Icon
    }
    Icon(
        imageVector = icon,
        contentDescription = "LV$level",
        modifier = modifier.size(width = width, height = height),
        tint = Color.Unspecified,
    )
}

private fun userLevelIcon(
    name: String,
    fillColor: Color,
    pathData: String,
): ImageVector = ImageVector.Builder(
    name = name,
    defaultWidth = 24.dp,
    defaultHeight = 24.dp,
    viewportWidth = 1024f,
    viewportHeight = 1024f,
).apply {
    addPath(
        fill = SolidColor(fillColor),
        pathData = PathParser().parsePathString(pathData).toNodes(),
    )
}.build()

